<?php
// === Security.php - Módulo de sanitização simples ===

// Sanitiza qualquer entrada (formulário, querystring etc.)
function sanitize_input($data) {
    if (is_array($data)) {
        return array_map('sanitize_input', $data);
    }
    $data = trim($data);
    $data = stripslashes($data);
    $data = htmlspecialchars($data, ENT_QUOTES, 'UTF-8');
    return $data;
}
